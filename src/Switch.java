public class Switch {
    public static void main(String[]args){

        int num = 35 ;
            switch (num){
                case 1: System.out.println("15");
                break;
                case 2: System.out.println("25");
                break;
                case 3: System.out.println("35");
                break;
                default: System.out.println("Not Exit");

            }
    }
}
